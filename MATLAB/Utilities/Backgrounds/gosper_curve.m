function [x,y] = gosper_curve(n)
    x = zeros(2,7^n+1);
    X = zeros(2,7^n+1);
    x(1,2) = 1;
    M = [0;0];
    D = 1;
    T = acos((7+6.25-3/4)/(2*2.5*sqrt(7)));
    r = [cos(T) -sin(T);sin(T) cos(T)];
    R = [cos(-T) -sin(-T);sin(-T) cos(-T)];
    f = [cos(pi/3) -sin(pi/3);sin(pi/3) cos(pi/3)];
    F = [cos(-pi/3) -sin(-pi/3);sin(-pi/3) cos(-pi/3)];
    for m=1:n
        if m > 1
            d = zeros(1,size(D,2));
            b = flip(D == 0,2);
            D = [D b b D D D b];
        end
        for p=2:7^(m-1)+1
            X(:,7*(p-1)+1) = r*x(:,p)*sqrt(2.5^2+(sqrt(3)/2)^2);
        end
        x = X;
        for o=2:7^(m-1)+1
            if D(1,o-1) == 1
                x(:,7*(o-1)-5) = x(:,7*(o-2)+1)+R*(x(:,7*(o-1)+1)-x(:,7*(o-2)+1))/sqrt(7);
                x(:,7*(o-1)-4) = x(:,7*(o-1)-5)+f*(x(:,7*(o-1)-5)-x(:,7*(o-2)+1));
                x(:,7*(o-1)-3) = x(:,7*(o-1)-4)-(x(:,7*(o-1)-5)-x(:,7*(o-2)+1));
                x(:,7*(o-1)-2) = x(:,7*(o-1)-3)+f*(x(:,7*(o-1)-4)-x(:,7*(o-1)-5));
                x(:,7*(o-1)-1) = x(:,7*(o-1)-2)+(x(:,7*(o-1)-5)-x(:,7*(o-2)+1));
                x(:,7*(o-1)) = x(:,7*(o-1)-2)+2*(x(:,7*(o-1)-5)-x(:,7*(o-2)+1));
            elseif D(1,o-1) == 0
                x(:,7*(o-1)-5) = x(:,7*(o-2)+1)+R*F*(x(:,7*(o-1)+1)-x(:,7*(o-2)+1))/sqrt(7);
                x(:,7*(o-1)-4) = x(:,7*(o-1)-5)+f*(x(:,7*(o-1)-5)-x(:,7*(o-2)+1));
                x(:,7*(o-1)-3) = x(:,7*(o-1)-5)+2*f*(x(:,7*(o-1)-5)-x(:,7*(o-2)+1));
                x(:,7*(o-1)-2) = x(:,7*(o-1)-3)-(x(:,7*(o-1)-5)-x(:,7*(o-2)+1));
                x(:,7*(o-1)-1) = x(:,7*(o-1)-2)-(x(:,7*(o-1)-4)-x(:,7*(o-1)-5));
                x(:,7*(o-1)) = x(:,7*(o-1)-1)+f*f*(x(:,7*(o-1)-5)-x(:,7*(o-2)+1));
            end
        end
    end
    y = x(2,:)';
    x = x(1,:)';
end
